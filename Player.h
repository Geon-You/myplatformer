#pragma once
#include "Object.h"

class Player :
    public Object
{
private:
    float speed; // 플레이어의 이동속도
    list<LINE*>* lineListAddress; // 메인 게임에서 넘겨주는 라인 리스트 주소를 넘겨받을 멤버
    float time; // 점프 시, 가속도 표현을 위해 사용
    bool isJump; // 현재 점프 상태인지를 나타내기 위해 사용
    Object* obstacle; // 메인 게임에서 박스 정보를 받아옴

public:
    void SetLineList(list<LINE*>* lineListAddress) { this->lineListAddress = lineListAddress; } // 메인 게임에서 리스트 주소를 가져오는 메서드
    void SetObstacle(Object* obstacle) { this->obstacle = obstacle; } // 메인 게임에서 장애물 객체의 주소를 가져오는 메서드

public:
    // Object을(를) 통해 상속됨
    void Initialize() override;
    void Update() override;
    void Render(HDC hdc) override;
    void Release() override;

private:
    void CheckKey(); // 키 입력 체크
    void Jump(); // 점프 기능
    void BoxColl(); // 박스 충돌 감지
    void LineColl(); // 라인 충돌 감지

public:
    Player();
    virtual ~Player();
};

